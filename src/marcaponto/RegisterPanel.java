    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
        try
        {
            conn = ConexaoMySQL.conectarBanco();
            Statement st = conn.createStatement();
            
            ResultSet rs = st.executeQuery(
                "SELECT usuario FROM projeto_java.Usuarios WHERE usuario = \"" + tfUsername.getText() + "\";"
            );
            if (rs.next())
                throw new Exception("Nome de usu√°rio j√° existe");
           
            
            //checar tamanho da senha
            if (pfPassword.getPassword().length < 8)
                throw new Exception("Senha muito curta");
            
            //checar se senhas batem
            if (!(new String(pfPassword.getPassword())).contentEquals(new String(pfConfirmPassword.getPassword())))
                throw new Exception("Senhas n√£o s√£o iguais");
            
        
            //inserir novo usuario
            String sql = "insert into projeto_java.Usuarios (nome, cpf, telefone, email, usuario, senha, admin) values (";
            sql += "\"" + tfNome.getText() + "\", ";
            sql += "\"" + tfCPF.getText() + "\", ";
            sql += "\"" + tfTelefone.getText() + "\", ";
            sql += "\"" + tfEmail.getText() + "\", ";
            sql += "\"" + tfUsername.getText() + "\", ";
            sql += "\"" + new String(pfPassword.getPassword()) + "\", ";
            sql += (isAdmin ? '1' : '0') + ");";
            st.executeUpdate(sql);
            
            if (!isAdmin) {
                sql = "select * from projeto_java.Usuarios where usuario = \"" + tfUsername.getText() + "\"";
                rs = st.executeQuery(sql);
                rs.next();
                sql = "insert into projeto_java.Pontos (id_usuario, estado) values (";
                sql += rs.getInt("id") + ", 0)";
                st.executeUpdate(sql);
            }
            
            mainFrame.switchPanel("LOGIN");
        }
        catch (SQLException e)
        {
            System.out.println(e.getMessage());
        }
        catch (Exception e) {
            lbErro.setText(e.getMessage());
            lbErro.setVisible(true);
        }
        finally {
            if (conn != null) {
                try {
                    conn.close();
                }
                catch (SQLException e) {
                    System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

